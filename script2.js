function showDetails(city) {
    let detailsText = "";

    if (city === "Львів") {
        detailsText = "Львів — одне з найкрасивіших міст України, відоме своєю старовинною архітектурою та історією. Якщо ви любите поєднувати прогулянки з відвідинами культурних пам'яток, Львів — це чудовий вибір.";
    } else if (city === "Київ") {
        detailsText = "Київ — столиця України, місто з багатою історією та культурними пам'ятками. Прогулянки по Києву подарують вам незабутні враження від відвідування таких місць, як Печерська лавра, Ботанічний сад і численні парки.";
    } else if (city === "Карпати") {
        detailsText = "Карпати — ідеальне місце для любителів природи та активного відпочинку. Тури по горах, ліси та мальовничі пейзажі зроблять ваш похід незабутнім.";
    }

    document.getElementById("details-text").textContent = detailsText;
    document.getElementById("details").style.display = "block";
}
